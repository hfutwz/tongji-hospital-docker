# 无效地址关键词（直接返回 None）
INVALID_KEYWORDS = {'无', '未知', '未备注', '自行', '家', '家中', '*', '0', '(跳过)'}


def extract_district(text: str) -> "str | None":
    """
    从地址文本解析上海行政区
    返回：区名（如"宝山区"）或 None（无法识别）

    策略（优先级从高到低）：
    1. 直接包含区名（"宝山区"/"浦东新区"）
    2. 包含区名简写（"宝山"/"浦东"）
    3. 包含已知路名/地名，映射到区
    """
    if not text:
        return None

    text = str(text).strip()

    # 过滤无效值
    if text in INVALID_KEYWORDS or len(text) <= 1:
        return None

    # 1. 完整区名匹配
    for d in DISTRICT_LIST:
        if d in text:
            return d

    # 2. 路名/地名 → 区映射
    # 防止"静安路"被误判为静安区：若关键词 <= 2字 且文本是"关键词+路/街/道"结构，跳过
    import re as _re
    for keyword, district in ROAD_DISTRICT_MAP.items():
        if keyword in text:
            # 防误判：短关键词 + 紧跟"路/街/道"，说明是路名而非区域名
            if len(keyword) <= 2 and _re.search(rf'{_re.escape(keyword)}[路街道]', text):
                continue
            return district

    return None


def resolve_location(longitude, latitude, injury_location: str) -> dict:
    """
    综合解析地点信息，返回地点层级和区名

    Returns:
        {
            'level': 0/1/2,
            'district': '宝山区' or None,
            'has_coordinate': bool
        }
    """
    # Level 2: 已有经纬度
    if longitude and latitude:
        try:
            lng = float(longitude)
            lat = float(latitude)
            if 120 < lng < 122 and 30 < lat < 32:   # 上海经纬度范围
                # 由经纬度反查区（简化：用文本兜底）
                district = extract_district(injury_location)
                return {'level': 2, 'district': district, 'has_coordinate': True}
        except (ValueError, TypeError):
            pass

    # Level 1: 文本推断区
    district = extract_district(injury_location)
    if district:
        return {'level': 1, 'district': district, 'has_coordinate': False}

    # Level 0: 仅全市兜底
    return {'level': 0, 'district': None, 'has_coordinate': False}
